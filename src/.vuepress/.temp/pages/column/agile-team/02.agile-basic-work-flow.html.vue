<template><div><h1 id="summary" tabindex="-1"><a class="header-anchor" href="#summary"><span>Summary</span></a></h1>
<p>敏捷开发流程中的一些很好的工作方式，本文中使用的敏捷方法为 Scrum</p>
<h1 id="建卡" tabindex="-1"><a class="header-anchor" href="#建卡"><span>建卡</span></a></h1>
<p>BA 负责建 story 卡，如果其他人需要建立卡片，需要告知 BA</p>
<h1 id="需求澄清-three-amigos" tabindex="-1"><a class="header-anchor" href="#需求澄清-three-amigos"><span>需求澄清（three amigos）</span></a></h1>
<p>BA DEV QA 三方 catchup ，澄清卡的需求，然后 BA 移动到当前迭代</p>
<h1 id="工作量估算-estimation" tabindex="-1"><a class="header-anchor" href="#工作量估算-estimation"><span>工作量估算（estimation）</span></a></h1>
<p>BA 发起全员来进行估算工作量，并澄清需求</p>
<p>估点数量按照斐波那契数列例如 1 2 3 5，超过 5 个点的卡需要被拆分</p>
<h1 id="移动任务到待开发-ready-for-dev" tabindex="-1"><a class="header-anchor" href="#移动任务到待开发-ready-for-dev"><span>移动任务到待开发 （ready for dev）</span></a></h1>
<p>BA 把卡移动到 ready for dev，然后开发就可以开始工作</p>
<h1 id="开发中-development" tabindex="-1"><a class="header-anchor" href="#开发中-development"><span>开发中 （development）</span></a></h1>
<p>Dev 需要和 BA、QA 领卡，kick off 然后开始工作，每个卡需要建立一个branch，完成工作后需要检查 pipeline 是否通过然后，创建 Pull request，然后该卡可以移动到 review</p>
<h1 id="审查-review" tabindex="-1"><a class="header-anchor" href="#审查-review"><span>审查 （Review）</span></a></h1>
<p>Review 环节需要找人 approve PR</p>
<h1 id="移动任务到待测试-ready-for-test" tabindex="-1"><a class="header-anchor" href="#移动任务到待测试-ready-for-test"><span>移动任务到待测试 （Ready for test）</span></a></h1>
<p>合并代码到 master，和 QA sign off 后移动故事卡到 Ready for test</p>
</div></template>


